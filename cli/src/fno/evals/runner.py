def _run_single_task(
    task: TaskSpec,
    workdir: Path,
    *,
    loop_script: Path,
    label: str,
    model: str,
    keep_workdir: bool,
    history_path: Path,
    repo_root: Path,
    abilities_sha: str | None,
    installed_rev_val: str | None,
    real_state_paths: dict[str, Path] | None = None,
    claude_projects_dir: Path | None = None,
) -> dict:
    """Run one golden task end-to-end and append a history row.

    Returns the row dict that was written.  Always appends a row, even on
    harness errors or timeouts, so the "no-gaps" invariant holds.

    Args:
        real_state_paths:   Injected dict of real-state paths for the
                            detective isolation check.  Defaults to
                            ``default_real_state_paths(repo_root)``.
                            Tests supply fake paths here.
        claude_projects_dir: Override for ``~/.claude/projects`` base
                             directory for transcript discovery.
    """
    start_time = time.monotonic()

    # Per-task progress header
    print(f"\n[eval] task: {task.slug}  budget: ${task.budget_usd}  "
          f"max-iter: {task.max_iterations}  timeout: {task.timeout_secs}s",
          flush=True)

    # ------------------------------------------------------------------ #
    # Init workdir                                                         #
    # ------------------------------------------------------------------ #
    try:
        _init_workdir(task, workdir)
        _init_manifest(task, workdir)
    except Exception as exc:
        print(f"[eval] workdir init failed: {exc}", flush=True)
        termination_reason = "harness-error"
        assertions_list: list = []
        session_id: str | None = None
        tokens_total: int | None = None
        cost_usd: float | None = None
        transcript_path: str | None = None
        wall_secs = time.monotonic() - start_time
        row = _build_row(
            task=task,
            label=label,
            abilities_sha=abilities_sha,
            installed_rev_val=installed_rev_val,
            model=model,
            termination_reason=termination_reason,
            assertions_list=assertions_list,
            tokens_total=tokens_total,
            cost_usd=cost_usd,
            wall_secs=wall_secs,
            iterations=None,
            session_id=session_id,
            transcript_path=transcript_path,
            workdir=workdir,
            keep_workdir=keep_workdir,
        )
        append_row(history_path, row)
        if not keep_workdir:
            shutil.rmtree(workdir, ignore_errors=True)
        return row

    # ------------------------------------------------------------------ #
    # Run the loop script                                                  #
    # ------------------------------------------------------------------ #
    loop_rc: int = -1
    interrupted = False
    termination_reason: str | None = None

    cmd = [
        str(loop_script),
        "--max-iterations", str(task.max_iterations),
        "--budget", str(task.budget_usd),
    ]
    if model:
        cmd += ["--model", model]

    proc: subprocess.Popen | None = None
    try:
        proc = subprocess.Popen(
            cmd,
            cwd=workdir,
            start_new_session=True,
            # Belt-and-braces with the .path-migration-done sentinel: any
            # fno invocation inside the spawned session must not rewrite
            # the workdir settings fragment (isolation overrides).
            env=_eval_subprocess_env(),
        )
        try:
            loop_rc = proc.wait(timeout=task.timeout_secs)
        except subprocess.TimeoutExpired:
            if proc.pid:
                _kill_process_group(proc.pid)
            try:
                proc.wait(timeout=5)
            except Exception:
                pass
            loop_rc = -1
            termination_reason = "timeout"
            print(f"[eval] task {task.slug!r} timed out after {task.timeout_secs}s", flush=True)

    except KeyboardInterrupt:
        interrupted = True
        if proc is not None:
            try:
                pid = proc.pid
                if pid:
                    _kill_process_group(pid)
            except Exception:
                pass
            try:
                proc.wait(timeout=5)
            except Exception:
                pass
        # Write the Interrupted row then re-raise so run_tasks can clean up
        wall_secs = time.monotonic() - start_time
        row = _build_row(
            task=task,
            label=label,
            abilities_sha=abilities_sha,
            installed_rev_val=installed_rev_val,
            model=model,
            termination_reason="Interrupted",
            assertions_list=[],
            tokens_total=None,
            cost_usd=None,
            wall_secs=wall_secs,
            iterations=None,
            session_id=None,
            transcript_path=None,
            workdir=workdir,
            keep_workdir=True,  # always keep on interrupt
        )
        append_row(history_path, row)
        raise  # re-raise for run_tasks interrupt handler

    # ------------------------------------------------------------------ #
    # Determine termination reason (unless already set by timeout)        #
    # ------------------------------------------------------------------ #
    if not interrupted and termination_reason is None:
        termination_reason = _read_termination_reason(workdir, loop_rc)

    # ------------------------------------------------------------------ #
    # Run assertions                                                       #
    # ------------------------------------------------------------------ #
    assertions_list, assert_output = _run_assert_sh(task, workdir)
    # Echo assertion lines to stdout for visibility
    for line in assert_output.splitlines():
        stripped = line.strip()
        if stripped.startswith(("ok ", "not ok ")):
            print(f"  {stripped}", flush=True)

    # ------------------------------------------------------------------ #
    # Collect eval session ids + transcript path (detective layer input)  #
    # ------------------------------------------------------------------ #
    eval_session_ids, transcript_path = collect_eval_session_ids(
        workdir,
        claude_projects_dir=claude_projects_dir,
    )

    # ------------------------------------------------------------------ #
    # Collect cost (session-cost.py resolves by transcript UUID)           #
    # ------------------------------------------------------------------ #
    transcript_uuid = Path(transcript_path).stem if transcript_path else None
    tokens_total, cost_usd, session_id = _get_cost(workdir, transcript_uuid)
    # Also add the session_id from the events.jsonl scan, in case
    # collect_eval_session_ids missed it (belt and braces).
    if session_id:
        eval_session_ids.add(session_id)

    # ------------------------------------------------------------------ #
    # Detective isolation check                                            #
    # ------------------------------------------------------------------ #
    if real_state_paths is None:
        real_state_paths = default_real_state_paths(repo_root)
    isolation_result = check_isolation(eval_session_ids, real_state_paths)

    if isolation_result.verdict == "violated":
        report = format_violation_report(isolation_result)
        print(report, flush=True)

    print(
        f"[eval] isolation: {isolation_result.verdict}",
        flush=True,
    )

    wall_secs = time.monotonic() - start_time

    # ------------------------------------------------------------------ #
    # Build and write row                                                  #
    # ------------------------------------------------------------------ #
    row = _build_row(
        task=task,
        label=label,
        abilities_sha=abilities_sha,
        installed_rev_val=installed_rev_val,
        model=model,
        termination_reason=termination_reason,
        assertions_list=assertions_list,
        tokens_total=tokens_total,
        cost_usd=cost_usd,
        wall_secs=wall_secs,
        iterations=None,
        session_id=session_id,
        transcript_path=transcript_path,
        isolation_verdict=isolation_result.verdict,
        workdir=workdir,
        keep_workdir=keep_workdir or termination_reason in (
            "harness-error", "timeout",
        ) or not all(a.ok for a in assertions_list) or len(assertions_list) == 0,
    )
    append_row(history_path, row)

    # Cleanup workdir if requested
    if not row.get("workdir_kept"):
        shutil.rmtree(workdir, ignore_errors=True)

    # Print per-task summary line
    pass_count = sum(1 for a in assertions_list if a.ok)
    total_count = len(assertions_list)
    status = "PASS" if row["passed"] else "FAIL"
    print(
        f"[eval] {task.slug}: {status}  {pass_count}/{total_count} assertions  "
        f"reason={termination_reason}  wall={wall_secs:.1f}s",
        flush=True,
    )

    return row


def _build_row(
    *,
    task: TaskSpec,
    label: str,
    abilities_sha: str | None,
    installed_rev_val: str | None,
    model: str,
    termination_reason: str,
    assertions_list: list,
    tokens_total: int | None,
    cost_usd: float | None,
    wall_secs: float,
    iterations: int | None,
    session_id: str | None,
    transcript_path: str | None,
    isolation_verdict: str = "unknown",
    workdir: Path,
    keep_workdir: bool,
) -> dict:
    """Construct the history row dict."""
    passed = len(assertions_list) > 0 and all(a.ok for a in assertions_list)
    assertions_dict = {a.name: a.ok for a in assertions_list}
    workdir_kept_val = str(workdir) if keep_workdir else None

    return {
        "ts": _ts_now(),
        "task": task.slug,
        "label": label,
        "abilities_sha": abilities_sha,
        "installed_rev": installed_rev_val,
        "model": model,
        "driver": "claude-code",
        "termination_reason": termination_reason,
        "assertions": assertions_dict,
        "passed": passed,
        "total": len(assertions_list),
        "tokens_total": tokens_total,
        "cost_usd": cost_usd,
        "wall_secs": round(wall_secs, 2),
        "iterations": iterations,
        "session_id": session_id,
        "transcript_path": transcript_path,
        "isolation": isolation_verdict,
        "workdir_kept": workdir_kept_val,
    }


