"""Golden-task eval runner - pure orchestration logic.

The public entry point is ``run_tasks``.  All external processes are
invoked via the standard ``subprocess`` module so unit tests can patch
``subprocess.Popen`` and substitute a stub loop script via the
``loop_script`` parameter (never the real ``run-target-loop.sh``).

Injection points
----------------
``loop_script``
    Path to the loop driver.  Resolved at call time from
    ``FNO_EVALS_LOOP_SCRIPT`` env var, then ``<repo_root>/scripts/run-target-loop.sh``.
    Callers (tests and CLI) can override by passing the path directly.

``history_path``
    Destination for history rows.  Defaults to ``paths.evals_history()``.
    Tests supply a tmp path via the monkeypatched ``paths_testing`` fixture.

``real_state_paths``
    Dict of real operator state paths for the detective isolation check.
    Defaults to ``isolation.default_real_state_paths(repo_root)``.
    Tests supply a dict of tmp-path fakes so tests never read/write
    ``~/.fno`` or ``~/.claude``.

``claude_projects_dir``
    Base directory for Claude transcript projects (``~/.claude/projects``
    by default).  Tests supply a tmp path to avoid real transcript files.

Isolation contract
------------------
The PREVENTIVE layer (``_write_workdir_settings``) redirects all
Python ``fno.paths``-aware writers inside the eval session into
the workdir.  It covers:
- ``config.paths.graph_json``  -> workdir/.fno/graph.json
- ``config.paths.ledger_json`` -> workdir/.fno/ledger.json
- ``config.paths.briefs_dir``  -> workdir/.fno/briefs/
- ``config.paths.evals_history`` -> workdir/.fno/evals-history.jsonl
- ``config.state_dir``         -> workdir/.fno/ (default fallback)

It CANNOT cover these known escapees (bash-layer and hardcoded writers):
- ``scripts/metrics/register-task.py:23``: LEDGER_JSON_PATH hardcodes
  the home-state ledger path (``~/.fno/ledger.json``) - no config
  read, no FNO_HOME.
- ``scripts/lib/paths.sh:11-13`` (auto-generated by ``fno paths
  emit-shell``): ``STATE_DIR="$HOME/.fno"`` hardcoded; all
  bash-layer writers inherit this.

The DETECTIVE layer (``fno.evals.isolation``) is therefore the
load-bearing backstop.  It runs post-assertions and attributes any eval
session id found in real state files as a violation.  A violation causes
a nonzero exit even when assertions passed.
"""
