def _ts_now() -> str:
    """Return current UTC time as an ISO-8601 string."""
    return datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")


def _git_sha(repo_root: Path) -> str | None:
    """Return HEAD SHA of *repo_root*, or None on failure."""
    try:
        result = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            cwd=repo_root,
            capture_output=True,
            text=True,
            timeout=10,
        )
        if result.returncode == 0:
            return result.stdout.strip()
    except Exception:
        pass
    return None


def _installed_rev() -> str | None:
    """Read ``installed-rev`` from the state dir, return None if absent."""
    try:
        from fno import paths as _paths

        p = _paths.state_dir() / "installed-rev"
        if p.exists():
            return p.read_text(encoding="utf-8").strip() or None
    except Exception:
        pass
    return None


def _abi_doctor_verdict() -> str:
    """Run ``fno doctor`` and return its first stdout line, or 'unavailable' on failure."""
    try:
        result = subprocess.run(
            ["fno", "doctor"],
            capture_output=True,
            text=True,
            timeout=15,
        )
        first_line = (result.stdout or result.stderr or "").strip().splitlines()
        return first_line[0] if first_line else "unavailable"
    except Exception:
        return "unavailable"


def _find_repo_root(start: Path) -> Path:
    """Walk up from *start* to find the git repo root.

    Falls back to ``git rev-parse --show-toplevel`` when the standard
    ``.git`` marker is found.
    """
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--show-toplevel"],
            cwd=start,
            capture_output=True,
            text=True,
            timeout=10,
        )
        if result.returncode == 0:
            return Path(result.stdout.strip())
    except Exception:
        pass
    # Fallback: walk up looking for .git
    current = start.resolve()
    while current != current.parent:
        if (current / ".git").exists():
            return current
        current = current.parent
    return start


def _resolve_loop_script(repo_root: Path) -> Path:
    """Return the resolved loop script path.

    Priority: ``FNO_EVALS_LOOP_SCRIPT`` env var, then
    ``<repo_root>/scripts/run-target-loop.sh``.
    """
    env_override = os.environ.get("FNO_EVALS_LOOP_SCRIPT")
    if env_override:
        return Path(env_override)
    return repo_root / "scripts" / "run-target-loop.sh"


def _validate_loop_script(script: Path) -> None:
    """Raise ``RunnerError`` when *script* is missing or not executable."""
    if not script.exists():
        raise RunnerError(
            f"loop script not found: {script}\n"
            "Set FNO_EVALS_LOOP_SCRIPT env var to point at a valid loop script."
        )
    mode = script.stat().st_mode
    if not (mode & (stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)):
        raise RunnerError(
            f"loop script is not executable: {script}\n"
            "Run: chmod +x " + str(script)
        )


def _read_termination_reason(workdir: Path, loop_rc: int) -> str:
    """Determine the termination reason from events.jsonl or loop exit code."""
    events_path = workdir / ".fno" / "events.jsonl"
    if events_path.exists():
        last_reason: str | None = None
        for line in events_path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line:
                continue
            try:
                event = json.loads(line)
                if event.get("type") == "termination":
                    last_reason = event.get("data", {}).get("reason")
            except (json.JSONDecodeError, AttributeError):
                continue
        if last_reason:
            return last_reason

    # Fall back to exit-code mapping
    if loop_rc == 0:
        return "unknown-complete"
    if loop_rc == 1:
        return "MaxIterations"
    if loop_rc in (2, 77):
        return "harness-error"
    return "harness-error"


def _eval_subprocess_env() -> dict[str, str]:
    """Build the env for eval-session subprocesses (loop + manifest init).

    - ``FNO_SKIP_MIGRATION=1``: any fno invocation inside the spawned
      session must not rewrite the workdir settings fragment.
    - Scrubs ``FNO_CONFIG`` and ``FNO_REPO_ROOT``: when inherited
      from the operator's shell, either pins config resolution AWAY from
      the workdir fragment (``FNO_CONFIG`` short-circuits as the
      ONLY candidate; ``FNO_REPO_ROOT`` re-anchors worktree-root
      resolution), silently bypassing the preventive isolation layer.
      Scrubbing keeps the documented chain: workdir fragment first,
      global fallthrough intact.  (Codex P2 on PR #451.)
    """
    env = {**os.environ, "FNO_SKIP_MIGRATION": "1"}
    env.pop("FNO_CONFIG", None)
    env.pop("FNO_REPO_ROOT", None)
    return env


def _write_workdir_settings(workdir: Path) -> None:
    """Write .fno/settings.yaml into *workdir* for isolation.

    Sets ``no_ship: true`` and redirects all mutable paths inside the
    workdir so no eval run touches real user state.

    PREVENTIVE COVERAGE (Python ``fno.paths``-aware writers only)
    --------------------------------------------------------------------
    This fragment is honoured by any writer that calls
    ``fno.paths.*`` with ``CWD = workdir`` at resolution time.
    Explicit per-path overrides are belt-and-braces on top of
    ``config.state_dir`` (evals_history has its own key and is not
    derived from state_dir):
    - ``config.state_dir``          -> workdir/.fno/
    - ``config.paths.graph_json``   -> workdir/.fno/graph.json
    - ``config.paths.ledger_json``  -> workdir/.fno/ledger.json
    - ``config.paths.briefs_dir``   -> workdir/.fno/briefs/
    - ``config.paths.evals_history``-> workdir/.fno/evals-history.jsonl

    KNOWN ESCAPEES (cannot be redirected by this fragment)
    -------------------------------------------------------
    - ``scripts/metrics/register-task.py:23``: LEDGER_JSON_PATH
      hardcodes the home-state ledger path
      (``~/.fno/ledger.json``) - no config read, no
      FNO_HOME.
    - ``scripts/lib/paths.sh:11-13`` (auto-generated by
      ``fno paths emit-shell``): ``STATE_DIR="$HOME/.fno"``
      hardcoded; all bash-layer writers inherit this.

    Do NOT attempt HOME or FNO_HOME manipulation on the spawned
    session - this breaks claude CLI auth and the bash layer ignores
    FNO_HOME anyway.  The detective layer
    (``fno.evals.isolation``) is the load-bearing backstop.
    """
    abilities_dir = workdir / ".fno"
    abilities_dir.mkdir(parents=True, exist_ok=True)

    settings: dict = {
        "schema_version": 1,
        "config": {
            "no_ship": True,
            "state_dir": str(abilities_dir) + "/",
            "paths": {
                "graph_json": str(abilities_dir / "graph.json"),
                "ledger_json": str(abilities_dir / "ledger.json"),
                "briefs_dir": str(abilities_dir / "briefs/"),
                "evals_history": str(abilities_dir / "evals-history.jsonl"),
            },
        },
    }
    settings_path = abilities_dir / "settings.yaml"
    settings_path.write_text(yaml.dump(settings, default_flow_style=False), encoding="utf-8")

    # Suppress fno's first-invocation path migration inside the eval
    # session.  ``_check_migration`` (cli.py) keys off
    # ``state_dir()/.path-migration-done``; without this sentinel the
    # first ``fno`` call in the spawned session REWRITES this fragment
    # (nulls the per-path overrides, resets state_dir to ~/.fno/),
    # silently undoing the preventive layer.  Observed live in the first
    # smoke sweep (2026-06-05).
    (abilities_dir / ".path-migration-done").touch()


def _init_workdir(task: TaskSpec, workdir: Path) -> None:
    """Populate *workdir* with repo contents, plan.md, git repo, settings."""
    # Copy repo/ contents into workdir root
    repo_src = task.path / "repo"
    for item in repo_src.iterdir():
        dst = workdir / item.name
        if item.is_dir():
            shutil.copytree(item, dst)
        else:
            shutil.copy2(item, dst)

    # Copy plan.md
    shutil.copy2(task.path / "plan.md", workdir / "plan.md")

    # Write isolation settings
    _write_workdir_settings(workdir)

    # Git init + initial commit
    subprocess.run(["git", "init", "-q"], cwd=workdir, check=True, capture_output=True)
    subprocess.run(
        ["git", "config", "user.email", "evals@fno.local"],
        cwd=workdir, check=True, capture_output=True,
    )
    subprocess.run(
        ["git", "config", "user.name", "fno evals"],
        cwd=workdir, check=True, capture_output=True,
    )
    subprocess.run(["git", "add", "-A"], cwd=workdir, check=True, capture_output=True)
    subprocess.run(
        ["git", "commit", "-q", "-m", f"eval: {task.slug} initial state"],
        cwd=workdir, check=True, capture_output=True,
    )
    # Create a non-main branch (init-target-state.sh refuses main/master + .git dir)
    subprocess.run(
        ["git", "checkout", "-q", "-b", "eval-run"],
        cwd=workdir, check=True, capture_output=True,
    )


def _init_manifest(task: TaskSpec, workdir: Path) -> None:
    """Pre-init the target-state manifest using init-target-state.sh."""
    repo_root = _find_repo_root(Path(__file__))
    init_script = repo_root / "hooks" / "helpers" / "init-target-state.sh"
    if not init_script.exists():
        # Best-effort: write a minimal manifest so the loop script finds it
        abilities_dir = workdir / ".fno"
        abilities_dir.mkdir(parents=True, exist_ok=True)
        manifest = abilities_dir / "target-state.md"
        manifest.write_text(
            "---\n"
            f"plan_path: {workdir / 'plan.md'}\n"
            "no_ship: true\n"
            "---\n",
            encoding="utf-8",
        )
        return

    env = {
        **_eval_subprocess_env(),
        "TARGET_START": "1",
        "TARGET_INPUT": str(workdir / "plan.md"),
        "TARGET_PLAN_PATH": str(workdir / "plan.md"),
        "TARGET_SIZE": "S",
        "TARGET_NO_SHIP": "1",
    }
    result = subprocess.run(
        ["bash", str(init_script)],
        cwd=workdir,
        env=env,
        capture_output=True,
        timeout=30,
    )
    if result.returncode != 0:
        raw_stderr = result.stderr or b""
        if isinstance(raw_stderr, bytes):
            stderr_snippet = raw_stderr.decode("utf-8", errors="replace")[:200]
        else:
            stderr_snippet = str(raw_stderr)[:200]
        print(
            f"[eval] init-target-state.sh exited {result.returncode}: {stderr_snippet}",
            file=sys.stderr,
            flush=True,
        )


def _run_assert_sh(task: TaskSpec, workdir: Path) -> tuple[list, str]:
    """Run assert.sh in *workdir* and return (assertions, raw_output).

    Failure modes:
    - TimeoutExpired: prints ``[eval] assert.sh timed out after 120s`` to
      stderr and returns ([], partial_stdout) where partial_stdout is whatever
      bytes the process emitted before timing out (may be empty).
    - Other exceptions (OSError, etc.): prints ``[eval] assert.sh failed to
      run: {exc}`` to stderr and returns ([], "").
    - Completed with returncode != 0 and zero assertions: prints
      ``[eval] assert.sh exited {rc} with no TAP output`` to stderr
      (distinguishes crashed-script from ran-clean-asserted-nothing).
    """
    assert_sh = task.path / "assert.sh"
    try:
        result = subprocess.run(
            ["bash", str(assert_sh)],
            cwd=workdir,
            capture_output=True,
            text=True,
            timeout=120,
            # Fixture assert.sh scripts use "${PYTHON:-python3}"; inject the
            # interpreter running this runner (has pytest) so assertions are
            # deterministic regardless of the ambient python3 site-packages.
            env={**os.environ, "PYTHON": sys.executable},
        )
    except subprocess.TimeoutExpired as exc:
        # Decode partial stdout if present (may be bytes or None)
        partial: str = ""
        if exc.stdout is not None:
            partial = exc.stdout.decode("utf-8", errors="replace") if isinstance(exc.stdout, bytes) else (exc.stdout or "")
        print(f"[eval] assert.sh timed out after 120s", file=sys.stderr, flush=True)
        return parse_tap(partial), partial
    except Exception as exc:
        print(f"[eval] assert.sh failed to run: {exc}", file=sys.stderr, flush=True)
        return [], ""

    output = result.stdout + result.stderr
    assertions = parse_tap(output)
    if result.returncode != 0 and len(assertions) == 0:
        print(
            f"[eval] assert.sh exited {result.returncode} with no TAP output",
            file=sys.stderr,
            flush=True,
        )
    return assertions, output


def _get_cost(
    workdir: Path,
    transcript_uuid: str | None = None,
) -> tuple[int | None, float | None, str | None]:
    """Retrieve tokens/cost for the eval session from fno.cost._session_cost.

    ``fno.cost._session_cost`` resolves transcripts by Claude transcript UUID
    (the ``.jsonl`` filename stem under ``~/.claude/projects/``), NOT by
    the fno-internal session id found in events.jsonl.  Callers
    should pass ``transcript_uuid`` from transcript discovery
    (``collect_eval_session_ids``); without it the lookup cannot
    succeed and tokens/cost are returned as None.

    Returns (tokens_total, cost_usd, session_id) where ``session_id``
    is the fno-internal id scanned from the workdir's
    events.jsonl (used for the history row + isolation attribution).
    """
    # session_id from workdir events.jsonl
    session_id: str | None = None
    events_path = workdir / ".fno" / "events.jsonl"
    if events_path.exists():
        for line in events_path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line:
                continue
            try:
                event = json.loads(line)
                sid = event.get("session_id") or event.get("data", {}).get("session_id")
                if sid:
                    session_id = sid
            except (json.JSONDecodeError, AttributeError):
                continue

    if not transcript_uuid:
        return None, None, session_id

    # Cost via the in-package _session_cost module (`python3 -m`), which resolves
    # from the installed wheel with no repo on disk.
    try:
        result = subprocess.run(
            [sys.executable, "-m", "fno.cost._session_cost", "--json", transcript_uuid],
            capture_output=True,
            text=True,
            timeout=15,
        )
        if result.returncode == 0:
            data = json.loads(result.stdout)
            tokens = data.get("tokens", {}).get("total")
            cost = data.get("cost_usd")
            return tokens, cost, session_id
    except (json.JSONDecodeError, AttributeError, subprocess.TimeoutExpired, OSError):
        pass
    return None, None, session_id


def _kill_process_group(pid: int) -> None:
    """Send SIGKILL to the process group of *pid* (best-effort)."""
    try:
        os.killpg(os.getpgid(pid), signal.SIGKILL)
    except (ProcessLookupError, PermissionError, OSError):
        try:
            os.kill(pid, signal.SIGKILL)
        except (ProcessLookupError, PermissionError, OSError):
            pass


