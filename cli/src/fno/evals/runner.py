def run_tasks(
    *,
    fixtures_dir: Path,
    task_slug: Optional[str],
    label: str,
    model: str,
    keep_workdir: bool,
    loop_script: Optional[Path] = None,
    history_path: Optional[Path] = None,
    real_state_paths: Optional[dict] = None,
    claude_projects_dir: Optional[Path] = None,
) -> int:
    """Discover and run golden eval tasks.

    Args:
        fixtures_dir:       Directory containing ``<slug>/`` fixture subdirs.
        task_slug:          When set, run only this fixture slug.  Non-existent
                            slug raises :class:`RunnerError` before any row is
                            written.
        label:              Freeform run label stored in every history row.
        model:              Model name passed to the loop script.
        keep_workdir:       Always keep workdirs (even on success).
        loop_script:        Override the loop driver.  When None, resolved from
                            ``FNO_EVALS_LOOP_SCRIPT`` env var or repo default.
        history_path:       Override the history file path.  When None, uses
                            ``paths.evals_history()``.
        real_state_paths:   Injectable dict of real-state paths for the
                            detective isolation check.  When None, uses
                            ``default_real_state_paths(repo_root)``.  Tests
                            supply fake paths here so tests never read/write
                            ``~/.fno`` or ``~/.claude``.
        claude_projects_dir: Override for ``~/.claude/projects`` base
                             directory.  When None, uses the real path.
                             Tests supply a tmp path here.

    Returns:
        0 if all tasks passed all assertions and no harness errors; 1 otherwise.

    Raises:
        RunnerError: On unrecoverable setup errors (missing loop script, bad slug).
    """
    from fno import paths as _paths

    if history_path is None:
        history_path = _paths.evals_history()

    # Resolve repo root from the fixtures_dir location
    repo_root = _find_repo_root(fixtures_dir)

    # Resolve loop script
    if loop_script is None:
        loop_script = _resolve_loop_script(repo_root)

    # Upfront: reject missing/non-executable loop script
    _validate_loop_script(loop_script)

    # Discover tasks
    all_tasks = discover_fixtures(fixtures_dir)

    if task_slug is not None:
        matching = [t for t in all_tasks if t.slug == task_slug]
        if not matching:
            available = ", ".join(t.slug for t in all_tasks)
            raise RunnerError(
                f"no fixture found for --task {task_slug!r}. "
                f"Available: {available or '(none)'}"
            )
        tasks_to_run = matching
    else:
        tasks_to_run = all_tasks

    # Collect shared metadata once
    abilities_sha = _git_sha(repo_root)
    installed_rev_val = _installed_rev()

    # Run header
    doctor_verdict = _abi_doctor_verdict()
    print(f"\n{'='*60}", flush=True)
    print(f"fno evals run", flush=True)
    print(f"  tasks:     {len(tasks_to_run)}", flush=True)
    print(f"  label:     {label}", flush=True)
    print(f"  model:     {model}", flush=True)
    print(f"  loop:      {loop_script}", flush=True)
    print(f"  doctor:    {doctor_verdict}", flush=True)
    print(f"{'='*60}", flush=True)

    rows: list[dict] = []
    completed_slugs: set[str] = set()

    try:
        for task in tasks_to_run:
            # resolve(): macOS mkdtemp returns /var/folders/... but Claude
            # Code records the realpath (/private/var/...) in its project
            # dir encoding; transcript discovery needs the resolved path.
            workdir = Path(tempfile.mkdtemp(prefix=f"abi-eval-{task.slug}-")).resolve()
            try:
                row = _run_single_task(
                    task,
                    workdir,
                    loop_script=loop_script,
                    label=label,
                    model=model,
                    keep_workdir=keep_workdir,
                    history_path=history_path,
                    repo_root=repo_root,
                    abilities_sha=abilities_sha,
                    installed_rev_val=installed_rev_val,
                    real_state_paths=real_state_paths,
                    claude_projects_dir=claude_projects_dir,
                )
                rows.append(row)
                completed_slugs.add(task.slug)
            except KeyboardInterrupt:
                # _run_single_task already wrote the Interrupted row; re-raise
                raise
            except Exception as exc:
                print(f"[eval] ERROR running {task.slug}: {exc}", file=sys.stderr, flush=True)
                # Write an error row so no-gaps invariant holds
                error_row = _build_row(
                    task=task,
                    label=label,
                    abilities_sha=abilities_sha,
                    installed_rev_val=installed_rev_val,
                    model=model,
                    termination_reason="harness-error",
                    assertions_list=[],
                    tokens_total=None,
                    cost_usd=None,
                    wall_secs=0.0,
                    iterations=None,
                    session_id=None,
                    transcript_path=None,
                    workdir=workdir,
                    keep_workdir=True,
                )
                append_row(history_path, error_row)
                rows.append(error_row)
                completed_slugs.add(task.slug)

    except KeyboardInterrupt:
        print("\n[eval] interrupted - partial results written to history", flush=True)
        sys.exit(1)

    # ------------------------------------------------------------------ #
    # Summary table                                                        #
    # ------------------------------------------------------------------ #
    _print_summary(rows)

    # Exit code: 0 only if every task passed all assertions, no harness errors,
    # and no isolation violations
    all_passed = all(
        r["passed"]
        and r["termination_reason"] not in ("harness-error", "timeout")
        and r.get("isolation") != "violated"
        for r in rows
    )
    return 0 if all_passed else 1


def _print_summary(rows: list[dict]) -> None:
    """Print a formatted summary table to stdout."""
    if not rows:
        print("\n[eval] No tasks ran.", flush=True)
        return

    print(f"\n{'='*60}", flush=True)
    print("Summary", flush=True)
    print(f"{'='*60}", flush=True)
    header = f"{'task':<30} {'pass/total':<12} {'termination':<20} {'tokens':<10} {'cost':<10} {'isolation':<12}"
    print(header, flush=True)
    print("-" * len(header), flush=True)
    for row in rows:
        tokens = str(row.get("tokens_total") or "-")
        cost_str = f"${row['cost_usd']:.4f}" if row.get("cost_usd") is not None else "-"
        pass_total = f"{sum(1 for v in row['assertions'].values() if v)}/{row['total']}"
        line = (
            f"{row['task']:<30} {pass_total:<12} "
            f"{row['termination_reason']:<20} {tokens:<10} "
            f"{cost_str:<10} {row['isolation']:<12}"
        )
        print(line, flush=True)
    print(f"{'='*60}", flush=True)
    total = len(rows)
    passed = sum(1 for r in rows if r["passed"])
    print(f"Result: {passed}/{total} tasks passed", flush=True)
