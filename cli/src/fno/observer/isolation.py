"""Detective attribution check for replay-run isolation.

Resurrected verbatim from ``cli/src/fno/evals/isolation.py`` (cut in
``675b24e``); the observer harness (x-57a5, Locked Decision 5) reuses its
proven preventive+detective isolation mechanics, not the golden-fixture eval
concept it was part of. "eval run" below reads as "replay run".

Scans real operator state files for replay session ids that should never
appear there.  If any replay session id is found in a real-state surface,
the run has violated isolation.

Public API
----------
``collect_eval_session_ids(workdir, *, claude_projects_dir)``
    Gather every session id attributable to an eval run from three sources:
    - the workdir ``target-state.md`` manifest (``session_id:`` frontmatter)
    - the workdir ``.fno/events.jsonl`` (any ``session_id`` field)
    - transcript ``.jsonl`` files under
      ``<claude_projects_dir>/<encoded-workdir>/``
    Returns ``(ids: set[str], transcript_path: str | None)`` where
    ``transcript_path`` is the first transcript file found (fills the
    history row's ``transcript_path`` field).

``check_isolation(eval_session_ids, real_state_paths)``
    Scan real-state paths for any occurrence of the eval session ids.
    Returns an ``IsolationResult`` with a ``verdict`` and ``violations``.

Known-escapee context
---------------------
The PREVENTIVE layer (``_write_workdir_settings``) covers Python
``fno.paths``-aware writers that read the workdir-local
``settings.yaml`` when ``CWD = workdir``.  It CANNOT cover:

- ``scripts/metrics/register-task.py:23``: LEDGER_JSON_PATH hardcodes
  the home-state ledger path (``~/.fno/ledger.json``) - no config
  read, no FNO_HOME.
- ``scripts/lib/paths.sh:11-13`` (auto-generated by ``fno paths
  emit-shell``): ``STATE_DIR="$HOME/.fno"`` hardcoded; all
  bash-layer writers inherit this.

The DETECTIVE layer (this module) is therefore the load-bearing backstop.
A follow-up carveout (filed via ``fno carveout add``) tracks the decision
of whether to add config-path awareness to these writers.
"""
from __future__ import annotations

import json
import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional


# ---------------------------------------------------------------------------
# Data types
# ---------------------------------------------------------------------------


@dataclass
class Violation:
    """One occurrence of an eval session id in a real operator state file.

    Attributes:
        path:         Absolute path to the file containing the leak.
        session_id:   The eval session id that was found.
        line_number:  1-based line number of the first match in the file.
                      For whole-file (non-JSONL) scans, ``1`` if anywhere
                      in the file.
        detail:       One-line explanation of the suspected escape path,
                      naming the known-escapee writer where applicable.
    """

    path: Path
    session_id: str
    line_number: int
    detail: str


@dataclass
class IsolationResult:
    """Result of a single isolation check.

    Attributes:
        verdict:     ``"clean"`` if no eval ids were found in any real-state
                     surface; ``"violated"`` if at least one was.
        violations:  List of :class:`Violation` objects, one per (file, id)
                     pair detected.  Empty when ``verdict == "clean"``.
    """

    verdict: str  # "clean" | "violated"
    violations: list[Violation] = field(default_factory=list)

    def __post_init__(self) -> None:
        """Enforce coherence: verdict must be 'violated' iff violations non-empty."""
        has_violations = len(self.violations) > 0
        if self.verdict == "violated" and not has_violations:
            raise ValueError(
                "IsolationResult coherence error: verdict='violated' requires non-empty violations"
            )
        if self.verdict == "clean" and has_violations:
            raise ValueError(
                "IsolationResult coherence error: verdict='clean' requires empty violations"
            )


# ---------------------------------------------------------------------------
# Known-escapee metadata
# ---------------------------------------------------------------------------

# Maps a "surface key" (matches keys in ``real_state_paths``) to a one-line
# escape explanation used in violation details.
_ESCAPE_EXPLANATIONS: dict[str, str] = {
    "ledger_json": (
        "writer bypasses project-local path overrides - known escapee: "
        "register-task.py ledger registration (scripts/metrics/register-task.py:23)"
    ),
    "graph_json": (
        "writer bypasses project-local path overrides - known escapee: "
        "paths.sh STATE_DIR (scripts/lib/paths.sh:11-13)"
    ),
    "repo_events_jsonl": (
        "writer bypasses project-local path overrides - known escapee: "
        "paths.sh STATE_DIR bash-layer writers"
    ),
    "global_events_jsonl": (
        "writer bypasses project-local path overrides - known escapee: "
        "paths.sh STATE_DIR bash-layer writers"
    ),
    "memory_dir": (
        "memory-pass or autocorrect writer may have used real session context"
    ),
    "corrections_log": (
        "autocorrect corrections.log writer may bypass project-local isolation"
    ),
}

_DEFAULT_ESCAPE_EXPLANATION = (
    "writer bypasses project-local path overrides - check known escapees: "
    "register-task.py (scripts/metrics/register-task.py:23) and "
    "paths.sh STATE_DIR (scripts/lib/paths.sh:11-13)"
)


# ---------------------------------------------------------------------------
# Internal scan helpers
# ---------------------------------------------------------------------------


def _scan_jsonl_file(
    path: Path,
    eval_session_ids: set[str],
    surface_key: str,
) -> list[Violation]:
    """Scan a JSONL file line by line for eval session ids.

    Reports the 1-based line number of the first match per (id, file).
    Missing files are silently skipped (clean result).
    """
    if not path.exists():
        return []

    violations: list[Violation] = []
    seen_ids: set[str] = set()
    explanation = _ESCAPE_EXPLANATIONS.get(surface_key, _DEFAULT_ESCAPE_EXPLANATION)

    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return []

    for lineno, line in enumerate(text.splitlines(), start=1):
        stripped = line.strip()
        if not stripped:
            continue
        for sid in eval_session_ids:
            if sid in seen_ids:
                continue
            if sid in stripped:
                violations.append(
                    Violation(
                        path=path,
                        session_id=sid,
                        line_number=lineno,
                        detail=explanation,
                    )
                )
                seen_ids.add(sid)

    return violations


def _scan_whole_file(
    path: Path,
    eval_session_ids: set[str],
    surface_key: str,
) -> list[Violation]:
    """Scan a non-JSONL file (whole content) for eval session ids.

    Reports line number of the first matching line (1-based).
    Missing files are silently skipped.
    """
    if not path.exists():
        return []

    violations: list[Violation] = []
    seen_ids: set[str] = set()
    explanation = _ESCAPE_EXPLANATIONS.get(surface_key, _DEFAULT_ESCAPE_EXPLANATION)

    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return []

    lines = text.splitlines()
    for sid in eval_session_ids:
        if sid not in text:
            continue
        # Find first line containing the id
        for lineno, line in enumerate(lines, start=1):
            if sid in line:
                violations.append(
                    Violation(
                        path=path,
                        session_id=sid,
                        line_number=lineno,
                        detail=explanation,
                    )
                )
                seen_ids.add(sid)
                break

    return violations


def _scan_memory_dir(
    memory_dir: Path,
    eval_session_ids: set[str],
) -> list[Violation]:
    """Scan all files inside the memory directory for eval session ids.

    Missing directory is silently skipped.
    """
    if not memory_dir.exists() or not memory_dir.is_dir():
        return []

    violations: list[Violation] = []
    try:
        for mem_file in sorted(memory_dir.iterdir()):
            if mem_file.is_file():
                violations.extend(
                    _scan_whole_file(mem_file, eval_session_ids, "memory_dir")
                )
    except OSError:
        pass
    return violations


# ---------------------------------------------------------------------------
# Public: check_isolation
# ---------------------------------------------------------------------------


def check_isolation(
    eval_session_ids: set[str],
    real_state_paths: dict[str, Path],
) -> IsolationResult:
    """Scan real operator state surfaces for any eval session id leaks.

    Args:
        eval_session_ids:   Set of session id strings attributable to the
                            eval run.  Collected via
                            :func:`collect_eval_session_ids`.
        real_state_paths:   Dict mapping surface keys to absolute ``Path``
                            objects.  All known keys:

                            - ``ledger_json``       real ~/.fno/ledger.json
                            - ``graph_json``        real ~/.fno/graph.json
                            - ``repo_events_jsonl`` fno repo .fno/events.jsonl
                            - ``global_events_jsonl`` ~/.fno/events.jsonl
                            - ``memory_dir``        ~/.fno/memory/ directory
                            - ``corrections_log``   ~/.claude/corrections.log

                            Unknown keys are ignored.  Missing files are
                            silently skipped (clean).

    Returns:
        :class:`IsolationResult` with ``verdict="clean"`` or
        ``verdict="violated"`` and a list of :class:`Violation` objects.
    """
    if not eval_session_ids:
        return IsolationResult(verdict="clean", violations=[])

    all_violations: list[Violation] = []

    # --- JSONL surfaces ---
    for key in ("ledger_json", "graph_json", "repo_events_jsonl", "global_events_jsonl"):
        path = real_state_paths.get(key)
        if path is not None:
            all_violations.extend(_scan_jsonl_file(path, eval_session_ids, key))

    # --- memory directory (whole-file scan per file) ---
    memory_dir = real_state_paths.get("memory_dir")
    if memory_dir is not None:
        all_violations.extend(_scan_memory_dir(memory_dir, eval_session_ids))

    # --- corrections.log (whole-file scan) ---
    corrections_log = real_state_paths.get("corrections_log")
    if corrections_log is not None:
        all_violations.extend(_scan_whole_file(corrections_log, eval_session_ids, "corrections_log"))

    verdict = "violated" if all_violations else "clean"
    return IsolationResult(verdict=verdict, violations=all_violations)


