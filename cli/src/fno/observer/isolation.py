def format_violation_report(result: IsolationResult) -> str:
    """Format a human-readable report of isolation violations.

    Each violation line includes:
    - the offending file path
    - the eval session id found
    - the line number
    - the suspected escape explanation

    Returns an empty string when ``result.verdict == "clean"``.
    """
    if result.verdict == "clean":
        return ""

    lines = [
        f"[eval] ISOLATION VIOLATED: {len(result.violations)} leak(s) detected",
        "",
    ]
    for v in result.violations:
        lines.append(f"  file:       {v.path}")
        lines.append(f"  session_id: {v.session_id}")
        lines.append(f"  line:       {v.line_number}")
        lines.append(f"  suspected:  {v.detail}")
        lines.append("")

    lines.append(
        "[eval] Action: check whether the eval workdir settings.yaml was loaded "
        "by the session (CWD must equal the workdir).  Known escapees that cannot "
        "be redirected: register-task.py (scripts/metrics/register-task.py:23) "
        "and paths.sh STATE_DIR (scripts/lib/paths.sh:11-13)."
    )
    return "\n".join(lines)
