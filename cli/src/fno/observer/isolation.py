def _encode_path_for_claude_projects(path: Path) -> str:
    """Encode an absolute path to the Claude projects dir convention.

    Claude maps every non-[a-zA-Z0-9] character in the absolute path to '-'.
    Example: ``/tmp/abi-eval-task-abc`` -> ``-tmp-abi-eval-task-abc``
    """
    return re.sub(r"[^a-zA-Z0-9]", "-", str(path))


def collect_eval_session_ids(
    workdir: Path,
    *,
    claude_projects_dir: Optional[Path] = None,
) -> tuple[set[str], Optional[str]]:
    """Gather every session id attributable to the eval run in *workdir*.

    Sources (in order, merged into one set):
    1. ``workdir/.fno/target-state.md`` frontmatter ``session_id:`` field.
    2. ``workdir/.fno/events.jsonl``: every ``session_id`` field value.
    3. Transcript ``.jsonl`` files under
       ``<claude_projects_dir>/<encoded-workdir>/``.

    The transcript directory encoding matches Claude Code's convention:
    every character in the absolute workdir path that is not ``[a-zA-Z0-9]``
    is replaced with ``-``.

    Args:
        workdir:              Absolute path to the eval workdir.
        claude_projects_dir:  Override for the Claude projects base directory
                              (defaults to ``~/.claude/projects``).  Tests
                              supply a tmp path here to stay fully isolated.

    Returns:
        ``(ids, transcript_path)`` where ``ids`` is the set of collected
        session ids and ``transcript_path`` is the absolute path string of
        the first transcript ``.jsonl`` file found (or ``None`` if absent).
    """
    if claude_projects_dir is None:
        claude_projects_dir = Path.home() / ".claude" / "projects"

    ids: set[str] = set()
    transcript_path: Optional[str] = None

    # ------------------------------------------------------------------ #
    # 1. target-state.md manifest                                         #
    # ------------------------------------------------------------------ #
    manifest = workdir / ".fno" / "target-state.md"
    if manifest.exists():
        try:
            for line in manifest.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if stripped.startswith("session_id:"):
                    # Strip surrounding quotes: manifest values may be
                    # quoted (other state files store the id unquoted).
                    value = stripped[len("session_id:"):].strip().strip("'\"")
                    if value:
                        ids.add(value)
        except OSError:
            pass

    # ------------------------------------------------------------------ #
    # 2. workdir events.jsonl                                             #
    # ------------------------------------------------------------------ #
    events_path = workdir / ".fno" / "events.jsonl"
    if events_path.exists():
        try:
            for line in events_path.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if not stripped:
                    continue
                try:
                    event = json.loads(stripped)
                    sid = (
                        event.get("session_id")
                        or event.get("data", {}).get("session_id")
                    )
                    if sid and isinstance(sid, str):
                        ids.add(sid)
                except (json.JSONDecodeError, AttributeError):
                    continue
        except OSError:
            pass

    # ------------------------------------------------------------------ #
    # 3. Transcript files under ~/.claude/projects/<encoded-workdir>/     #
    # ------------------------------------------------------------------ #
    encoded = _encode_path_for_claude_projects(workdir)
    project_dir = claude_projects_dir / encoded
    if project_dir.is_dir():
        try:
            transcript_files = sorted(project_dir.glob("*.jsonl"))
            if transcript_files:
                primary = transcript_files[0]
                transcript_path = str(primary)
                # Also collect session ids from transcript filenames (UUID stems)
                for tf in transcript_files:
                    stem = tf.stem
                    if stem:
                        ids.add(stem)
        except OSError:
            pass

    return ids, transcript_path


# ---------------------------------------------------------------------------
# Default real-state paths (used by runner when not overridden in tests)
# ---------------------------------------------------------------------------


def default_real_state_paths(repo_root: Path) -> dict[str, Path]:
    """Return the canonical real-state paths for isolation checking.

    These are the REAL paths - NOT the workdir-local redirects.  The point
    is to scan the actual operator state for any leaked eval session ids.

    Args:
        repo_root: The fno repo root (for repo-local events.jsonl).
    """
    home = Path.home()
    abilities_home = home / ".fno"
    return {
        "ledger_json": abilities_home / "ledger.json",
        "graph_json": abilities_home / "graph.json",
        "repo_events_jsonl": repo_root / ".fno" / "events.jsonl",
        "global_events_jsonl": abilities_home / "events.jsonl",
        "memory_dir": abilities_home / "memory",
        "corrections_log": home / ".claude" / "corrections.log",
    }


